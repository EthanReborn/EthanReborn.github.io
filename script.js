function pluralize(word) {
    if(word[word.length -1] == "h" || word[word.length -1] == "o" || word[word.length -1] == "s"){
        return word += "es";
    }
    if(word == "xenomorph"){
        return "xenomorphs";
    }
    if(word == "cyclops"){
        return "cyclops";
    }
    else if(word[word.length -1] == "y"){
        word = word.slice(0, word.length - 1);
        word += "ies";
        return word;
    }
    else if(word == "man"){
        return "men";
    }
    else if(word == "woman"){
        return "women";
    }
    else if(word == "elf"){
        return "elves";
    }
    else {
        return word += "s";
    }
}

