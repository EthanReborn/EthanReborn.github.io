function pluralize(word) {
    if(word == "xenomorph"){
        return "xenomorphs";
    }
    if(word == "cyclops"){
        return "cyclops";
    }
    if(word == "psychopath"){
        return "psychopaths";
    }
    if(word == "donkey"){
        return "donkeys";
    }
    if(word == "poison"){
        return "poison";
    }
    if(word == "venom"){
        return "venom";
    }
    if(word == "shoes"){
        return "shoes";
    }
    if(word == "man"){
        return "men";
    }
    if(word == "claws"){
        return "claws";
    }
    if(word == "woman"){
        return "women";
    }
    if(word == "elf"){
        return "elves";
    }
    if(word == "knife"){
        return "knives";
    }
    if(word == "knuckles"){
        return "knuckles";
    }
    if(word == "night-vision"){
        return "night-vision";
    }
    if(word == "sleigh"){
        return "sleighs";
    }
    else if(word[word.length -1] == "h" || word[word.length -1] == "o" || word[word.length -1] == "s"){
        return word += "es";
    }
    else if(word[word.length -1] == "y"){
        word = word.slice(0, word.length - 1);
        word += "ies";
        return word;
    }
    else {
        return word += "s";
    }
}


